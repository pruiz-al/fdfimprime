#include "libft.h"

int	ft_isalnum(int c)
{
	int	n;

	n = 0;
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z'))
	{
		n = 1;
	}
	return (n);
}
